/// Calculates the crc-32 for rocket league replays. Not all CRC algorithms are the same. The crc
/// algorithm can be generated with the following parameters (pycrc):
///
/// - `Width` = 32
/// - `Poly` = 0x04c11db7
/// - `XorIn` = 0x10340dfe
/// - `ReflectIn` = False
/// - `XorOut` = 0xffffffff
/// - `ReflectOut` = False
///
/// This implementation is a slice by 8 from the unreal engine seen in Bakkes' CPPRP
/// (https://github.com/Bakkes/CPPRP/blob/58fc19a972a7a0af059407982bdf553cfe091831/CPPRP/CRC.h#L245)
pub fn calc_crc(data: &[u8]) -> u32 {
    let mut crc = !(0xefcb_f201_u32.swap_bytes());
    crc = data.chunks_exact(8).fold(crc, |acc, sl| {
        let top = u32::from_le_bytes([sl[0], sl[1], sl[2], sl[3]]);
        let one = top ^ acc;
        CRCTABLES_SB8_DEPRECATED[7][(one & 0xFF) as usize]
            ^ CRCTABLES_SB8_DEPRECATED[6][((one >> 8) & 0xFF) as usize]
            ^ CRCTABLES_SB8_DEPRECATED[5][((one >> 16) & 0xFF) as usize]
            ^ CRCTABLES_SB8_DEPRECATED[4][(one >> 24) as usize]
            ^ CRCTABLES_SB8_DEPRECATED[3][sl[4] as usize]
            ^ CRCTABLES_SB8_DEPRECATED[2][sl[5] as usize]
            ^ CRCTABLES_SB8_DEPRECATED[1][sl[6] as usize]
            ^ CRCTABLES_SB8_DEPRECATED[0][sl[7] as usize]
    });

    let left_over = data.len() % 8;
    crc = data[data.len() - left_over..].iter().fold(crc, |acc, &x| {
        (acc >> 8) ^ CRCTABLES_SB8_DEPRECATED[0][(u32::from(x) ^ (acc & 0xFF)) as usize]
    });

    (!crc).swap_bytes()
}

